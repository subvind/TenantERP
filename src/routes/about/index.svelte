          What does Full Stack Developer or FSD mean? Most people would probably tell you that it has something to do with the Front End and Back End of an application. As in the conversation between a client (smart phone, browser) and a server (data center). For me FSD means any source code that needs to be written or maintained; which most modern companies provide direct programming language level API's to interact with their business. This has added a lot of surface area for all FSDs to explore and code. There are many videos about this on YouTube. So Full Stack Developer could mean a lot of things. It just depends on what problems you are trying to solve and with what <a href="/solutions">Solutions</a>. In this about page I'll be talking about the area that I have explored and the path that I have taken through the land of Full Stack Development.
