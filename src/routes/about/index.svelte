          <br /><br />You would be supprised how much faster one can go on a bicycle with gears/shifting and aerodynamics. The same is true with FSDs because they must jump from one role/context to another (marketer, UI/UXer, tester, developer, stager, productioner) and still be performant. A good full stack developer has experience in each area and a desktop setup that gives them more real estate than what a fronty or backy might have. For instance, I have multiple dedicated machines, and a keyboard-video-mouse switch to jump between each machine, then I leave workspaces open inside each machine for each role/context in a FSDs lifecycle. With a single 27 inch monitor and a 4 button switch that can toggle between 4 machines ... I can jump between workspaces and more space is available for other things. Start ups are in demand of FSDs because they need not hire extra people. Big companies want to hire talented FSDs so atleast 1 person can explain the entire codebase. Other companies want FSDs because when a team of like minded people get together amazing things can be achievied.
